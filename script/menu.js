function entrar(){
   const principal = document.getElementById('entrar')
   principal.style.display = 'block'
   principal.style.width = '100vw'
   principal.style.height = '50vh'
   principal.style.textAlign = 'center'
   principal.style.alignContent = 'center'
   
}

function sair(){
   const principal = document.getElementById('entrar')
   principal.style.display = 'none'
}

